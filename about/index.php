<h1>О федерации спортивной акробатики Тульской области</h1>
<div class="oh">
	<img class="left" width="260" src="/img/logo-big.png" alt="" title="Федерация спортивной акробатики Тульской области">
	<p>Региональная общественная организация « Федерация спортивной акробатика Тульской области » (далее РОО"ФСАТО") , созданная с целью популяризации и развития спортивной акробатики среди различных групп населения Тулы и области, была организована в декабре 2015г.</p>
	<p>Устав "ФСАТО" утвержден Учредительным собранием 1 декабря 2015 года.</p>
	<p>Вид спорта — спортивная акробатика.</p>
	<p>ФСАТО является членом ФСАР, что подтверждено Сертификатом.</p>
